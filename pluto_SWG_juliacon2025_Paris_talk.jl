# ‚ïî‚ïê‚ï° 1729e7c1-8255-4c81-a840-cb1069da4043
function download_gitlab_folder_simple(gitlab_url, download_dir=mktempdir())

    # Parse URL
    parts = split(gitlab_url, "/")
    host = join(parts[1:3], "/")
    namespace = parts[4]
    project = parts[5]
    folder = parts[end]

    # Try GitLab API (might work even without auth for public repos)
    project_path = "$namespace%2F$project"  # URL encode the /
    api_url = "$host/api/v4/projects/$project_path/repository/tree?path=$folder&recursive=true"

    files = String[]

    # Download API response
    api_response = String(download(api_url, IOBuffer()).data)

    # Simple parsing - look for "name":"filename" patterns
    name_pattern = r"\"name\":\"([^\"]+)\""
    type_pattern = r"\"type\":\"([^\"]+)\""

    name_matches = collect(eachmatch(name_pattern, api_response))
    type_matches = collect(eachmatch(type_pattern, api_response))

    for (name_match, type_match) in zip(name_matches, type_matches)
        if type_match.captures[1] == "blob"  # It's a file, not a directory
            filename = name_match.captures[1]
            if contains(filename, ".") && !startswith(filename, ".")
                push!(files, filename)
            end
        end
    end

    files = unique(files)


    mkpath(download_dir)
    base_url = "$host/$namespace/$project/-/raw/main/$folder/"

    success_count = 0
    for file in files
        url = base_url * file
        local_path = joinpath(download_dir, file)

        try
            download(url, local_path)
            success_count += 1
        catch e

            # Try alternative URL patterns
            alt_urls = [
                "$host/$namespace/$project/-/raw/master/$folder/$file",  # master branch
                "$host/$namespace/$project/raw/main/$folder/$file",      # without -/
                "$host/$namespace/$project/raw/master/$folder/$file"     # master without -/
            ]

            downloaded = false
            for alt_url in alt_urls
                try
                    download(alt_url, local_path)
                    success_count += 1
                    downloaded = true
                    break
                catch
                    continue
                end
            end
        end
    end

    return joinpath.(download_dir, files)
end

